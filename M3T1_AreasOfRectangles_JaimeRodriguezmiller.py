def main():
    print ("This program will calculate the area " + \
           "of a rectangle.")
    width = int(input("Enter the width: "))
    height = int(input("Enter the height: "))
    
    # A = W * H
    area = width * height
	    
    # give the answer
    print("The area is:",area)


